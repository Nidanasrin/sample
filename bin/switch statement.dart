void main(){
  int day=3;
  switch(day){
    case 1:
      print("Sunday");
    break;
    case 2:
      print("Monday");
    break;
    case3:
      print("tuesday");
    break;
    case4:
      print("Wednesday");
    break;
    case5:
      print("Thursday");
    break;
    case6:
      print("friday");
    break;
    case7:
      print("saturday");
    break;
    default:
      print("invalid entry");
  }
}